            },
            error: function(xhr, ajaxOptions, thrownError) {
                //alert(thrownError);
                $('#showEmailSubscribeErrors').html('<label class="error_msg_a">Could not complete your request, some unknown error occurred</label>');
            }
        });
    }

    return false;
}
